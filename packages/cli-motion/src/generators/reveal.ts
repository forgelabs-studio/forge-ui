export function generateReveal(props: Record<string, unknown>): string {
  const duration = (props.duration as number) ?? 0.7
  const delay = (props.delay as number) ?? 0
  const ease = (props.ease as string) ?? 'easeOut'
  const once = (props.once as boolean) ?? true

  return `// ForgeReveal.tsx — generated by forge-motion CLI
// Edit this file freely — you own it.
// Re-generate with: npx @forgelabs-studio/motion update reveal

'use client'

import type { ReactNode } from 'react'
import { useRef } from 'react'
import { motion, useInView } from 'framer-motion'

interface ForgeRevealProps {
  children: ReactNode
  duration?: number
  delay?: number
  ease?: string
  once?: boolean
}

export function ForgeReveal({
  children,
  duration = ${duration},
  delay = ${delay},
  ease = '${ease}',
  once = ${once},
}: ForgeRevealProps) {
  const ref = useRef<HTMLDivElement>(null)
  const inView = useInView(ref, { once, amount: 0.3 })
  const prefersReduced =
    typeof window !== 'undefined'
      ? window.matchMedia('(prefers-reduced-motion: reduce)').matches
      : false

  return (
    // overflow:hidden is required for the clip-path to mask correctly
    <div style={{ overflow: 'hidden' }}>
      <motion.div
        ref={ref}
        initial={prefersReduced ? false : { clipPath: 'inset(0 100% 0 0)' }}
        animate={prefersReduced ? { clipPath: 'inset(0 0% 0 0)' } : inView ? { clipPath: 'inset(0 0% 0 0)' } : { clipPath: 'inset(0 100% 0 0)' }}
        transition={prefersReduced ? { duration: 0 } : { duration, delay, ease }}
      >
        {children}
      </motion.div>
    </div>
  )
}
`
}
