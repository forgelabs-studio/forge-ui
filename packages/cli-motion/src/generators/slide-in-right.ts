export function generateSlideInRight(props: Record<string, unknown>): string {
  const duration = (props.duration as number) ?? 0.6
  const delay = (props.delay as number) ?? 0
  const distance = (props.distance as number) ?? 48
  const ease = (props.ease as string) ?? 'easeOut'
  const once = (props.once as boolean) ?? true

  return `// ForgeSlideInRight.tsx — generated by forge-motion CLI
// Edit this file freely — you own it.
// Re-generate with: npx @forgelabs-studio/motion update slide-in-right

'use client'

import type { ReactNode } from 'react'
import { useRef } from 'react'
import { motion, useInView } from 'framer-motion'

interface ForgeSlideInRightProps {
  children: ReactNode
  duration?: number
  delay?: number
  distance?: number
  ease?: string
  once?: boolean
}

export function ForgeSlideInRight({
  children,
  duration = ${duration},
  delay = ${delay},
  distance = ${distance},
  ease = '${ease}',
  once = ${once},
}: ForgeSlideInRightProps) {
  const ref = useRef<HTMLDivElement>(null)
  const inView = useInView(ref, { once, amount: 0.3 })
  const prefersReduced =
    typeof window !== 'undefined'
      ? window.matchMedia('(prefers-reduced-motion: reduce)').matches
      : false

  return (
    <div style={{ overflow: 'hidden' }}>
      <motion.div
        ref={ref}
        initial={prefersReduced ? false : { opacity: 0, x: distance }}
        animate={prefersReduced ? { opacity: 1, x: 0 } : inView ? { opacity: 1, x: 0 } : { opacity: 0, x: distance }}
        transition={prefersReduced ? { duration: 0 } : { duration, delay, ease }}
      >
        {children}
      </motion.div>
    </div>
  )
}
`
}
