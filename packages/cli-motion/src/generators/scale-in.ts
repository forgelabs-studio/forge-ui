export function generateScaleIn(props: Record<string, unknown>): string {
  const duration = (props.duration as number) ?? 0.5
  const delay = (props.delay as number) ?? 0
  const scale = (props.scale as number) ?? 0.85
  const ease = (props.ease as string) ?? 'easeOut'
  const once = (props.once as boolean) ?? true

  return `// ForgeScaleIn.tsx — generated by forge-motion CLI
// Edit this file freely — you own it.
// Re-generate with: npx forge-motion update scale-in

'use client'

import { useRef } from 'react'
import { motion, useInView } from 'framer-motion'

interface ForgeScaleInProps {
  children: React.ReactNode
  duration?: number
  delay?: number
  scale?: number
  ease?: string
  once?: boolean
}

export function ForgeScaleIn({
  children,
  duration = ${duration},
  delay = ${delay},
  scale = ${scale},
  ease = '${ease}',
  once = ${once},
}: ForgeScaleInProps) {
  const ref = useRef<HTMLDivElement>(null)
  const inView = useInView(ref, { once, amount: 0.3 })

  return (
    <motion.div
      ref={ref}
      initial={{ opacity: 0, scale }}
      animate={inView ? { opacity: 1, scale: 1 } : { opacity: 0, scale }}
      transition={{ duration, delay, ease }}
    >
      {children}
    </motion.div>
  )
}
`
}
